#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	if (lst)
	{
		while (lst->next != NULL)
		{
			i++;
			lst = lst->next;
		}
		i++;
	}
	return (i);
}
